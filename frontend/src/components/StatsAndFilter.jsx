import React from "react";
import { FilterTypes } from "../lib/data.js";
import { Badge } from "./ui/badge";
import { Button } from "./ui/button";
import { Filter, SquarePen } from "lucide-react";

const StatsAndFilter = ({
  completedTasksCount = 0,
  activeTasksCount = 0,
  filter = "all",
}) => {
  return (
    <div className="flex flex-col items-start justify-between gap-4 sm:flex-row sm:items-center">
      {/* Stats */}
      <div className="flex gap-3">
        <Badge
          variant="secondary"
          className="bg-white/50 text-accent-foreground border-info/20"
        >
          {activeTasksCount} {FilterTypes.ACTIVE}
        </Badge>
        <Badge
          variant="secondary"
          className="bg-white/50 text-success border-success/20"
        >
          {completedTasksCount} {FilterTypes.COMPLETED}
        </Badge>
      </div>

      {/* Filter */}
      <div className="flex flex-col gap-2 sm:flex-row">
        {Object.keys(FilterTypes).map((type) => {
          return (
            <Button
              key={type}
              variant={filter === FilterTypes[type] ? "gradient" : "ghost"}
              size="sm"
              className="capitalize opacity-75"
            >
              <Filter className="size-4" />
              {FilterTypes[type]}
            </Button>
          );
        })}
      </div>
    </div>
  );
};

export default StatsAndFilter;
